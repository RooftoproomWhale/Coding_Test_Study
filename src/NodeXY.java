public class NodeXY {
    private int x;
    private int y;
    private int order;

    public NodeXY(int x, int y, int order) {
        this.x = x;
        this.y = y;
        this.order = order;
    }

    public int getOrder() {
        return order;
    }

    public int getX() {
        return x;
    }


    public int getY() {
        return y;
    }


}
